; Задача 1 
; Задан одномерный массив из 10 элементов: x1, x2, ..., x10, размещаемый в памяти за телом программы. 
; Вычислить сумму элементов массива и записать её в ячейку памяти, следующую за ячейкой памяти, содержащей последний элемент массива.


; Программа для вычисления суммы 10 элементов массива, начиная с адреса 30

LXI H, 0x0030         ; Устанавливаем адрес начала массива в регистр HL
MVI D, 10         ; Устанавливаем количество элементов (10) в регистр D
MVI A, 0          ; Обнуляем аккумулятор A для хранения суммы

LOOP: 
    ADD M        ; Добавляем значение из памяти по адресу HL к A
    INX H        ; Переходим к следующему элементу массива
    DCR D        ; Уменьшаем счетчик итераций на 1
    JNZ LOOP     ; Если счетчик не равен нулю, продолжаем цикл

MOV M, A          ; Сохраняем итоговую сумму по адресу, следующему за последним элементом
HLT           
