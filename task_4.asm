; Задача 4
; Вычисляем Y = (x3 - x2) + (x1 + x4)

; Вычисляем (x3 - x2)
LXI H, 0x0032       ; Адрес x3
MOV A, M            ; Загружаем x3 в A
LXI H, 0x0031       ; Адрес x2
SUB M               ; Вычисляем x3 - x2
MOV B, A            ; Сохраняем результат (x3 - x2) в регистре B

; Вычисляем (x1 + x4)
LXI H, 0x0030       ; Адрес x1
MOV A, M            ; Загружаем x1 в A
LXI H, 0x0033       ; Адрес x4
ADD M               ; Вычисляем x1 + x4
MOV C, A            ; Сохраняем результат (x1 + x4) в регистре C

; Складываем результаты
MOV A, B            ; Загружаем (x3 - x2) из регистра B в A
ADD C               ; Вычисляем (x3 - x2) + (x1 + x4)

; Сохраняем результат Y
LXI H, 0x0034       ; Адрес Y
MOV M, A            ; Сохраняем Y в память
HLT                 ; Завершение программы